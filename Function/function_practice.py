def grocery(c):
    if c == "tofu":  
        print("Tofu is cheap and has good calories")
    elif c == "meat":  
        print("Meat has the most calories")
    elif c == "salmon" or c == "white fish":  
        print("Seafood is expensive but is healthy")
    else:  
        print("Chicken has fewer calories than meat")

grocery("meat")
